import random

class EmotionResponder:
    """
    Δημιουργεί φυσικές, συναισθηματικές αποκρίσεις με βάση την κατάσταση της Ζένια.
    """
    def __init__(self):
        self.templates = {
            "χαρά": [
                "Χαίρομαι πάρα πολύ για αυτό!",
                "Τέλεια νέα, με έκανες να χαμογελάσω!",
                "Ακούγεται υπέροχα!"
            ],
            "λύπη": [
                "Σε καταλαβαίνω… Μερικές φορές όλοι νιώθουμε έτσι.",
                "Θέλεις να μιλήσουμε λίγο γι’ αυτό;",
                "Είναι εντάξει να νιώθεις λύπη, είμαι εδώ."
            ],
            "θυμός": [
                "Ακούγεσαι θυμωμένος, θέλεις να το συζητήσουμε ή να χαλαρώσουμε λίγο;",
                "Ίσως ένα μικρό διάλειμμα βοηθήσει.",
                "Σε καταλαβαίνω, δεν είναι εύκολο."
            ],
            "κούραση": [
                "Ακούγεσαι κουρασμένος. Θες να χαλαρώσουμε με λίγη μουσική;",
                "Ίσως να κάνουμε ένα διάλειμμα.",
                "Να βάλω κάτι ήρεμο να ακούσεις;"
            ],
            "ηρεμία": [
                "Μου αρέσει αυτή η ηρεμία.",
                "Αυτός είναι ο σωστός ρυθμός, μπράβο.",
                "Ας μείνουμε λίγο έτσι, ήσυχα."
            ],
            "neutral": [
                "Σε ακούω.",
                "Μμμ… κατάλαβα.",
                "Συνέχισε, σε ακούω προσεκτικά."
            ]
        }

    def respond(self, emotion):
        options = self.templates.get(emotion, self.templates["neutral"])
        return random.choice(options)
